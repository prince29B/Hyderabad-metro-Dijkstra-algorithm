#define NUM_STATIONS 56  // Define the number of stations as a constant

// Function to implement Dijkstra's algorithm
void dijkstra(int graph[NUM_STATIONS][NUM_STATIONS], int source, string stations[NUM_STATIONS]) {
    int distance[NUM_STATIONS];  // Array to store the shortest distance from source to each station

    // Initialize all distances to infinity (a large value)
    for(int i = 0; i < NUM_STATIONS; i++) {
        distance[i] = INT_MAX;
    }

    // Priority queue to store the stations to be processed
    priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>> > pq;
    distance[source] = 0;  // Distance to the source itself is 0
    pq.push({0, source});  // Push the source with distance 0 to the priority queue

    // Main loop to process the priority queue
    while(!pq.empty()) {
        int current_station = pq.top().second;  // Get the station with the minimum distance
        int current_distance = pq.top().first;  // Get the distance to this station
        pq.pop();  // Remove the station from the queue

        // If the current distance is greater than the stored distance, skip processing
        if(distance[current_station] < current_distance) continue;

        // Loop through all stations to find the adjacent stations
        for(int i = 0; i < NUM_STATIONS; i++) {
            if(graph[current_station][i] == 1) {  // Check if there's a direct path
                int adjacent_station = i;
                int path_cost = 1;  // The cost of moving to an adjacent station is 1

                // If the new path is shorter, update the distance and push to the queue
                if(distance[adjacent_station] > path_cost + current_distance) {
                    distance[adjacent_station] = path_cost + current_distance;
                    pq.push({distance[adjacent_station], adjacent_station});
                }
            }
        }
    }

    // Print the shortest distances from the source to all stations
    cout << "Minimum Number of Stations from " << stations[source] << " To every station" << endl;
    for(int i = 0; i < NUM_STATIONS; i++) {
        cout << "Minimum Stations from " << stations[source] << " To " << stations[i] << " are " << distance[i] << endl;
    }
}

